# Population Variance and Standard Deviation

import statistics as stats

variance = stats.pvariance([1, 3, 4, 2, 6, 5, 3, 4, 5,2])

deviation = stats.stdev([1, 3, 4, 2, 6, 5, 3, 4, 5,2])

print('Population Variance = ', (variance))
print('Population Standard Deviation = ', (deviation))
